async function connectDB() {
  if (!db) {
    await client.connect();
    db = client.db('DadosAssimilados');
  }
  return db;
}

async function setUserSkin(guildId, guildName, userId, userName, skinId) {
  const database = await connectDB();
  const collection = database.collection('skins');

  const validSkinId = emojiSkins[skinId] ? skinId : 'default';
  const skinName = skinNames[validSkinId] || 'Default';

  await collection.updateOne(
    { guildId, userId },
    {
      $set: {
        skinId: validSkinId,
        skinName,
        guildName,
        userName,
        updatedAt: new Date(),
      },
    },
    { upsert: true }
  );
}

async function getUserSkin(guildId, userId) {
  const database = await connectDB();
  const collection = database.collection('skins');
  const doc = await collection.findOne({ guildId, userId });
  return doc || { skinId: 'default', skinName: 'Default' };
}

function getEmoji(skinId, dado, numero) {
  return emojiSkins[skinId]?.[`d${dado}`]?.[numero] || `?(${numero})`;
}

